export function fmtCurrency(lang, n) {
  return I18N[lang].currency + ' ' + Math.round(n).toLocaleString();
}

export function catIndexFor(type, category) {
  const key = type === 'expense' ? 'catsExp' : 'catsInc';
  let i = I18N.zh[key].indexOf(category);
  if (i === -1) i = I18N.en[key].indexOf(category);
  if (i === -1) i = (type === 'expense' ? I18N.zh.catsExp.length : I18N.zh.catsInc.length) - 1;
  return i;
}
